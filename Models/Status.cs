namespace PDFWebEdit.Models
{
    /// <summary>
    /// A status.
    /// </summary>
    public class Status
    {
        /// <summary>
        /// Gets or sets a value indicating whether the success.
        /// </summary>
        /// <value>
        /// True if success, false if not.
        /// </value>
        public bool Success { get; set; }

        /// <summary>
        /// Gets or sets the message.
        /// </summary>
        /// <value>
        /// The message.
        /// </value>
        public string Message { get; set; }
    }
}
